=begin
	- write a method that takes an array as as argument
	- have the method reverse its elements in place - mutate the array that is passed in
	- the return value should be the same array object
	- #reverse methods may not be used

	EXAMPLES:
list = [1,2,3,4]
result = reverse!(list)
result == [4, 3, 2, 1] # true
list == [4, 3, 2, 1] # true
list.object_id == result.object_id # true

list = %w(a b e d c)
reverse!(list) == ["c", "d", "e", "b", "a"] # true
list == ["c", "d", "e", "b", "a"] # true

list = ['abc']
reverse!(list) == ["abc"] # true
list == ["abc"] # true

list = []
reverse!(list) == [] # true
list == [] # true

ALGO:
	-

=end

def reverse!(array)
	iterator = array.size / 2
	counter = 1
	iterator.times do |index|
		array[index], array[-counter] = array[-counter], array[index]
		counter += 1
	end
	array
end

list = [1,2,3,4]
result = reverse!(list)
p result == [4, 3, 2, 1] # true
p list == [4, 3, 2, 1] # true
p list.object_id == result.object_id # true

list = %w(a b e d c)
p reverse!(list) == ["c", "d", "e", "b", "a"] # true
p list == ["c", "d", "e", "b", "a"] # true

list = ['abc']
p reverse!(list) == ["abc"] # true
p list == ["abc"] # true

list = []
